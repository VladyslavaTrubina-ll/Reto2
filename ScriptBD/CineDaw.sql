create database cine_daw collate utf8mb4_spanish_ci; 

-- TABLA: Sala
-- =============================================
CREATE TABLE Sala (
    id_sala CHAR(5) PRIMARY KEY,
    nombre VARCHAR(50) NOT NULL,
    capacidad INT DEFAULT 0
);

--  TABLA: Pelicula
-- =============================================
CREATE TABLE Pelicula (
    id_pelicula varchar(3) PRIMARY KEY,
    Titulo varchar(50) not null,    
    duracion INT NOT NULL,
    genero VARCHAR(30),
    precio_base DECIMAL(6,2) NOT NULL
);


--  TABLA: Cliente
-- =============================================
CREATE TABLE Cliente (
    dni CHAR(9) PRIMARY KEY,
    nombre VARCHAR(50) NOT NULL,
    apellidos VARCHAR(80) NOT NULL,
    email VARCHAR(100) UNIQUE NOT NULL,
    contraseña BLOB(300) NOT NULL
);


--  TABLA: Sesion
-- ===================================================
CREATE TABLE Sesion (
    id_sesion VARCHAR(6) PRIMARY KEY,
    fecha DATE  NOT NULL,
    hora_inicio TIME  NOT NULL,
    hora_fin TIME NOT NULL,
    id_sala char(5) NOT NULL,
    id_pelicula char(3) NOT NULL,
    espectadores INT DEFAULT 0,
    precio_sesion DECIMAL(6,2) NOT NULL,
	CONSTRAINT FK_id_sala  FOREIGN KEY (id_sala) REFERENCES Sala(id_sala) ON UPDATE cascade,
	CONSTRAINT FK_id_pelicla FOREIGN KEY (id_pelicula) REFERENCES Pelicula(id_pelicula),
	CONSTRAINT uk_sesion_unica UNIQUE (fecha, hora_inicio, id_sala)

);

--  TABLA: Compra
-- ===================================================
CREATE TABLE Compra (
    id_compra INT AUTO_INCREMENT PRIMARY KEY,
    dni_cliente CHAR(9) NOT NULL,
    fecha_hora DATETIME NOT NULL,
    num_entradas int not null,
    precio_total DECIMAL(8,2) NOT NULL,
    descuento_aplicado DECIMAL(6,2) DEFAULT 0,
	CONSTRAINT FK_dni_cliente FOREIGN KEY (dni_cliente) REFERENCES Cliente(dni),
	CONSTRAINT ck_descuento_apliacado check (descuento_aplicado < precio_total)
);

--  TABLA: Entrada
-- ===================================================
CREATE TABLE Entrada (
    id_entrada INT AUTO_INCREMENT PRIMARY KEY,
    id_compra INT NOT NULL,
    id_sesion VARCHAR (6) NOT NULL,
    numero_personas INT NOT NULL,
    precio decimal(6,2) not null,
    descuento decimal (6,2) default 0.00,
    CONSTRAINT FK_id_sesion FOREIGN KEY (id_sesion) REFERENCES Sesion(id_sesion),
	CONSTRAINT FK_id_Compra  FOREIGN KEY (id_compra) REFERENCES Compra(id_compra),
	CONSTRAINT ck_descuento check (descuento < precio)
);


